radius = {}
n, *a = map(int, s.split())
for i in range(n):
    left = i - 1
    right = i + 1
    while left > -1 and a[left] <= a[i]:
        left -= 1
    while right < n and a[right] <= a[i]:
        right += 1
    length = right - left - 1
    if length not in radius:
        radius[length] = a[i]
    else:
        if radius[length] > a[i]:
            radius[length] = a[i]

